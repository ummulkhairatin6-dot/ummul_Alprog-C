# masukkan suhu anda dalam celcius
# masukkan suhu anda dalam fahrenheit
# masukkan suhu anda dalam reamur
# masukkan suhu anda dalam kelvin

# fahrenheit
celcius = float(input("masukkan suhu anda dalam celcius:"))
fahren = 9/5 * celcius + 32
print ("suhu anda dalam fahrenheit :" , fahren)

# reamur
celcius = float(input("masukkan suhu anda dalam celcius;"))
reamur = 4/5 * celcius 
print ("suhu anda dalam reamur :" , reamur)

# kelvin
celcius = float(input("masukkan suhu anda dalam celcius:"))
kelvin = celcius + 273
print ("suhu anda dalam kelvin :" , kelvin)