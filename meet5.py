#penjumlahan lambang (+)
a=13
b=9
nilai=a+b
print(a,"+",b,"=",nilai)

#pengurangan lambang (-)
a=24
b=2
nilai=a-b
print(a, "-",b, "=",nilai)

#perkalian lambang (*)
a=6
b=4
nilai=a * b
print(a, "*",b, "=", nilai)

#pembagian lambang (/)
a=32
b=2
nilai=a/b
print(a, "/",b,"=",nilai)

#perpangkatan lambang (**) 
a=3**8
b=3**6
nilai=a**b
print(a, "**",b,"=",nilai)

#modulus lambang (%) (sisa bagai)
a=30
b=4
nilai=a%b
print(a,"%",b,"=",nilai)

#floor division lambang (//) (pembulatan)
a=(4)
b=(2)
nilai=a//b
print(a,"//",b"=",nilai)

