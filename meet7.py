# komparasi = true / false
# > < >= <= != == is is-not

# komparasi dari (>) = lebih besar
print("========= lebih dari (>)")
x = 25
y = 86
hasil = x > 8
print(x, ">", 8, "=", hasil)

# komparasi nilai (<) = lebih kecil
print("========= kurang dari (<)")
x = 25
y = 86
hasil = x < 8
print(x, "<", 8, "=", hasil)

# komparasi dari (>=) = lebih besar dari 
print("========= lebih dari sama dengan (>=)")
x = 25
y = 86
hasil = x >= 8
print(x, ">=", 8, "=", hasil)

# komparasi dari (<=) = lebih kecil dari 
print("========= kurang dari sama dengan (<=)")
x = 25
y = 86
hasil = x <= 8
print(x, "<=", 8, "=", hasil)

# komparasi dari (!=) = tidak sama dgn
print("========= tidak sama dengan (!=)")
x = 25
y = 86
hasil = x != 8
print(x, "!=", 8, "=", hasil)

# komparasi dari (==) = perbandingan
print("========= sama dengan sama dengan (==)")
x = 25
y = 86
hasil = x == y
print(x, "==", y, "=", hasil)

# komparasi dari (is) = perbandingan
print("========= perbandingan (is)")
x = 25
y = 86
hasil = x is y
print(x, "is", y, "=", hasil)

# komparasi dari (is not)
print("========= tidak sama dengan (is not)")
x = 25
y = 86
hasil = x is not y
print(x, "is not", y, "=", hasil) 